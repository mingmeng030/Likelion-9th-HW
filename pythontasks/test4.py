#딕셔너리(내장함수)

pairs = {'잔나비' : '뜨거운 여름밤은 가고 남은 건 볼품 없지만',
'소하':'산책', '홍크':'어쩌면'}

# 하나의 키-밸류의 쌍을 추가하는 방법
# 딕셔너리형 변수 [키] = value

print(pairs)

pairs['스탠딩에그'] = '휴식'

print(pairs)
 
#특정 키-밸류 삭제하는 방법 : del 함수

# del 변수[키]

del pairs['잔나비']

print(pairs)

# 키로 밸류 얻기: 변수.get(키)

v = pairs.get('홍크')

